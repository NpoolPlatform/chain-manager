func CreateSet(c *ent.CoinExtraCreate, in *npool.CoinExtraReq) *ent.CoinExtraCreate {
	if in.ID != nil {
		c.SetID(uuid.MustParse(in.GetID()))
	}
	if in.CoinTypeID != nil {
		c.SetCoinTypeID(uuid.MustParse(in.GetCoinTypeID()))
	}
	if in.HomePage != nil {
		c.SetHomePage(in.GetHomePage())
	}
	if in.Specs != nil {
		c.SetSpecs(in.GetSpecs())
	}
	return c
}

func Create(ctx context.Context, in *npool.CoinExtraReq) (*ent.CoinExtra, error) {
	var info *ent.CoinExtra
	var err error

	_, span := otel.Tracer(constant.ServiceName).Start(ctx, "Create")
	defer span.End()

	defer func() {
		if err != nil {
			span.SetStatus(codes.Error, "db operation fail")
			span.RecordError(err)
		}
	}()

	span = tracer.Trace(span, in)

	err = db.WithClient(ctx, func(_ctx context.Context, cli *ent.Client) error {
		c := cli.CoinExtra.Create()
		info, err = CreateSet(c, in).Save(_ctx)
		return err
	})
	if err != nil {
		return nil, err
	}

	return info, nil
}

func CreateBulk(ctx context.Context, in []*npool.CoinExtraReq) ([]*ent.CoinExtra, error) {
	var err error
	rows := []*ent.CoinExtra{}

	_, span := otel.Tracer(constant.ServiceName).Start(ctx, "CreateBulk")
	defer span.End()

	defer func() {
		if err != nil {
			span.SetStatus(codes.Error, "db operation fail")
			span.RecordError(err)
		}
	}()

	span = tracer.TraceMany(span, in)

	err = db.WithTx(ctx, func(_ctx context.Context, tx *ent.Tx) error {
		bulk := make([]*ent.CoinExtraCreate, len(in))
		for i, info := range in {
			bulk[i] = CreateSet(tx.CoinExtra.Create(), info)
		}
		rows, err = tx.CoinExtra.CreateBulk(bulk...).Save(_ctx)
		return err
	})
	if err != nil {
		return nil, err
	}
	return rows, nil
}

func UpdateSet(info *ent.CoinExtra, in *npool.CoinExtraReq) *ent.CoinExtraUpdateOne {
	stm := info.Update()

	if in.HomePage != nil {
		stm = stm.SetHomePage(in.GetHomePage())
	}
	if in.Specs != nil {
		stm = stm.SetSpecs(in.GetSpecs())
	}

	return stm
}

func Update(ctx context.Context, in *npool.CoinExtraReq) (*ent.CoinExtra, error) {
	var info *ent.CoinExtra
	var err error

	_, span := otel.Tracer(constant.ServiceName).Start(ctx, "Create")
	defer span.End()

	defer func() {
		if err != nil {
			span.SetStatus(codes.Error, "db operation fail")
			span.RecordError(err)
		}
	}()

	span = tracer.Trace(span, in)

	err = db.WithTx(ctx, func(_ctx context.Context, tx *ent.Tx) error {
		info, err = tx.CoinExtra.Query().Where(coinextra.ID(uuid.MustParse(in.GetID()))).ForUpdate().Only(_ctx)
		if err != nil {
			return fmt.Errorf("fail query coinextra: %v", err)
		}

		stm := UpdateSet(info, in)

		info, err = stm.Save(_ctx)
		if err != nil {
			return fmt.Errorf("fail update coinextra: %v", err)
		}

		return nil
	})
	if err != nil {
		return nil, fmt.Errorf("fail update coinextra: %v", err)
	}

	return info, nil
}

